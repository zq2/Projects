# Network Requirements
# - Students must register their device(s) MAC address with the campus network
# - Students must be using non-public Wi-Fi networks to check in to class
# - To check in, students must be on the campus network and have the correct MAC address
# - Students must enter a one-time password (OTP) sent to their registered device
# - The OTP must be verified before marking attendance
# - The system should be able to handle many students
# - The system should be secure and not allow students to mark attendance for each other
